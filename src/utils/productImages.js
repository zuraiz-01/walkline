const toGallery = (product) => {
  if (!product || typeof product !== 'object') {
    return []
  }
  const mapped = galleriesByName[product.name]
  if (mapped?.length) {
    return mapped
  }
  const fallbacks = [product.image, product.hoverImage].filter(Boolean)
  return Array.from(new Set(fallbacks))
}

const applyProductImage = (product) => {
  const gallery = toGallery(product)
  if (!gallery.length) {
    return product
  }
  return {
    ...product,
    image: gallery[0],
    hoverImage: gallery[1] ?? gallery[0],
  }
}

export const getProductGallery = (product) => {
  const gallery = toGallery(product)
  if (gallery.length >= 4) {
    return gallery.slice(0, 4)
  }
  if (gallery.length > 0) {
    const filled = [...gallery]
    while (filled.length < 4) {
      filled.push(gallery[filled.length % gallery.length])
    }
    return filled
  }
  return []
}

export const applyProductImagesToCatalog = (catalog) => {
  if (!catalog || typeof catalog !== 'object') {
    return catalog
  }
  return {
    ...catalog,
    products: (catalog.products ?? []).map(applyProductImage),
    trending: (catalog.trending ?? []).map(applyProductImage),
  }
}

